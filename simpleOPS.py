def sum(one, two):
    return one + two

def difference(one, two):
    return one - two

def product(one, two):
    return one * two

def quotient(one, two):
    return one / two

def reminder(one, two):
    return one % two

one = int(input("Enter the first number"))
two = int(input("Enter the second number"))

print(sum(one, two))
print(difference(one, two))
print(product(one, two))
print(quotient(one, two))
print(reminder(one, two))
